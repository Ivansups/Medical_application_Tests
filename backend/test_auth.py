def test_password_hashing():
    """–¢–µ—Å—Ç —Ö–µ—à–∏—Ä–æ–≤–∞–Ω–∏—è –ø–∞—Ä–æ–ª–µ–π"""
    print("üîê –¢–µ—Å—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ —Ö–µ—à–∏—Ä–æ–≤–∞–Ω–∏—è –ø–∞—Ä–æ–ª–µ–π...")
    
    password = "test_password_123"
    hashed = get_password_hash(password)
    
    print(f"   –ü–∞—Ä–æ–ª—å: {password}")
    print(f"   –•–µ—à: {hashed[:50]}...")
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –ø–∞—Ä–æ–ª—å –ø—Ä–∞–≤–∏–ª—å–Ω–æ –≤–µ—Ä–∏—Ñ–∏—Ü–∏—Ä—É–µ—Ç—Å—è
    is_valid = verify_password(password, hashed)
    print(f"   –í–µ—Ä–∏—Ñ–∏–∫–∞—Ü–∏—è: {'‚úÖ' if is_valid else '‚ùå'}")
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –Ω–µ–ø—Ä–∞–≤–∏–ª—å–Ω—ã–π –ø–∞—Ä–æ–ª—å –Ω–µ –ø—Ä–æ—Ö–æ–¥–∏—Ç
    is_invalid = verify_password("wrong_password", hashed)
    print(f"   –ù–µ–ø—Ä–∞–≤–∏–ª—å–Ω—ã–π –ø–∞—Ä–æ–ª—å: {'‚ùå' if not is_invalid else '‚úÖ'}")
    
    return is_valid and not is_invalid

